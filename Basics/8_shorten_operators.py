# Shortened Operators
# Addition
x = 1
x += 1  # instead of x = x + 1
print(x)

# Subtraction
y = 1
y -= 1  # instead of y = y - 1
print(y)

# Multiplication
z = 1
z *= 3  # instead of z = z * 3
print(z)

# Power
a = 2
a **= 5  # instead of a = a ** 5
print(a)

# Division
b = 15
b /= 3  # instead of b = b / 3
print(b)

# Truncating Division
c = 15
c //= 3  # instead of c = c // 3
print(c)

# Modulo
d = 20
d %= 2  # instead of d = d % 2
print(d)

