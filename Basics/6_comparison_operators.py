# Equal Operator (==)
equal = 1 == 2
print(equal)

# Not Equal Operator (!=)
not_equal = 1 != 2
print (not_equal)

# Less than Operator (<)
less_than = 1 < 2
print(less_than)

# Greater than Operator (>)
greater_than = 1 > 2
print(greater_than)

# Less than or Equal Operator (<=)
less_than_or_equal = 1 <= 2
print (less_than_or_equal)

# Greater than or Equal Operator (>=)
greater_than_or_equal = 1 >= 2
print(greater_than_or_equal)